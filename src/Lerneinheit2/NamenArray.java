package Lerneinheit2;

public class NamenArray {
    public static void main(String[] args) {

        String[] namen = {"Abdelrhman", "Manfouo", "Köhn", "Rayya", "Khachan"};

        // Iteriere über das Array und gebe die Namen aus
        for (int i = 0; i < namen.length; i++) {
            System.out.println(namen[i]);
        }
    }
}
